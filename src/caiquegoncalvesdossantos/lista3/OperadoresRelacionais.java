
package collage.lista3;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println((a + 1) == b);
        System.out.println(a == (b + 1));
        System.out.println((a + 1) == b);
        System.out.println((a + 1) >= b);
    }
}